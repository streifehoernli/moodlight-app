package ch.zhaw.hhrt.moodlightreferenceapp;

public class RGBWColor {

    private int r;
    private int g;
    private int b;
    private int w;

    public RGBWColor(int r, int g, int b, int w) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.w = w;
    }

    public int getR() {
        return r;
    }

    public int getG() {
        return g;
    }

    public int getB() {
        return b;
    }

    public int getW() {
        return w;
    }
}
